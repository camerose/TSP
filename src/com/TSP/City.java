package com.TSP;

public class City {
    private int x_val;
    private int y_val;
    private String name;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setX_val(int x_val) {
        this.x_val = x_val;
    }

    public int getX_val() {
        return x_val;
    }

    public void setY_val(int y_val) {
        this.y_val = y_val;
    }

    public int getY_val() {
        return y_val;
    }
}
